class S 
{
	public static void main(String[] args) 
	{
		//variable declaration
		int i;
		//initialization or first time supplying the value
		i = -100;
		System.out.println(i);
		System.out.println(i);
		System.out.println(i);
		System.out.println(i);
		//re-initialization// after the first value(100) the next value (200)
		i = -200;
		System.out.println(i);
		System.out.println(i);
		System.out.println(i);
		System.out.println(i);
	}
}
